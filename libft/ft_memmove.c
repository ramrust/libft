#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = -1;
	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	if (dst == NULL && src == NULL)
		return (NULL);
	if (dst < src)
	{
		while (len > ++i)
			d[i] = s[i];
	}
	else
	{
		i = len;
		while (i > 0)
		{
			d[i - 1] = s[i - 1];
			i--;
		}
	}
	return (dst);
}
